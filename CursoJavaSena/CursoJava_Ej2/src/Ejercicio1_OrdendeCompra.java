    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
    
        // Obtener los valores de los campos de texto
    int cantidadProductos1 = Integer.parseInt(jTextCantidadProductos1.getText());
    double valorUnidadProducto1 = Double.parseDouble(jTextValorUnidadProducto1.getText());
    
    int cantidadProductos2 = Integer.parseInt(jTextCantidadProductos2.getText());
    double valorUnidadProducto2 = Double.parseDouble(jTextValorUnidadProducto2.getText());
    
    int cantidadProductos3 = Integer.parseInt(jTextCantidadProductos3.getText());
    double valorUnidadProducto3 = Double.parseDouble(jTextValorUnidadProducto3.getText());
    
    // Calcular el subtotal para cada producto
    double subtotalProducto1 = cantidadProductos1 * valorUnidadProducto1;
    double subtotalProducto2 = cantidadProductos2 * valorUnidadProducto2;
    double subtotalProducto3 = cantidadProductos3 * valorUnidadProducto3;
    
    // Calcular el precio total de todos los productos
    double precioTotal = subtotalProducto1 + subtotalProducto2 + subtotalProducto3;
    
    // Actualizar los campos de texto con los subtotales
    jTextValorProductos1.setText(String.valueOf(subtotalProducto1));
    jTextValorProductos2.setText(String.valueOf(subtotalProducto2));
    jTextValorProductos3.setText(String.valueOf(subtotalProducto3));
    
    // Actualizar el jLabelResultado con la concatenaci√≥n del nombre del cliente y el precio total
    String nombreCliente = jTextCliente.getText();
    String resultado = "Cliente: " + nombreCliente + " - El valor de su compra: " + String.valueOf(precioTotal);
    jLabelResultado.setText(resultado);
    
    }//GEN-LAST:event_jButtonCalcularActionPerformed

